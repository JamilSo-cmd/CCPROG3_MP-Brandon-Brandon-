public class Creature {
    //attributes
    private String name;
    private String type;
    private String family;
    private int evolutionLv;
    private int healthPoints;
    //methods
    public Creature (String name, String type, String family, int EL, int hp){

        this.name = name;
        this.type = type;
        this.family = family;
        this.evolutionLv = EL;
        this.healthPoints = hp;
    }


}
